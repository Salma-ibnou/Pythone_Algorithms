# --------------------------
# type()
# All Data in Python is Object
# --------------------------

## Integer (int):
print(type(10))  
print(type(100))  
print(type(-50))  

## Float Point Number (float):
print(type(100.9))  
print(type(1.950950)) 
print(type(-100.9595))

## String (str):
print(type("Hello Python"))  

## Boolean (bool):
print(type(10 == 10)) # => true 
print(type(10 == 1))  # => false 
## List (List):
print(type([1,2,3]))

##Tuple (tuple):
print(type((1,2,3,4))
)

##Dictionary (dict):
print(type({"One":1, "two":2,"tree":3}))  


